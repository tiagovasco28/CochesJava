    public void pintarTexto(String marca) {

        File raiz = new File("."); // ruta
        File camino = new File(raiz.getAbsoluteFile().getParentFile() + "\\src\\Terminos\\" + marca + ".txt");

        if (camino.exists() == true) {

            try {
                FileReader reader = new FileReader(camino);
                BufferedReader br = new BufferedReader(reader);
                txtSeleccionado.read(br, null);

            } catch (Exception e) {
                System.out.println("Archivo No encontrado");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Ruta No Existe");
        }

    }

    public void pintarImagen(String marca) {
        ImageIcon imgIcon = new ImageIcon(getClass().getResource("/Images/" + marca.toLowerCase() + "_img.jpg"));
        Image imgEscalada = imgIcon.getImage().getScaledInstance(imageLabel.getWidth(),
                imageLabel.getHeight(), Image.SCALE_SMOOTH);
        Icon iconoEscalado = new ImageIcon(imgEscalada);
        imageLabel.setIcon(iconoEscalado);
        this.repaint();
    }

